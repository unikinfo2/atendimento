set generator "GEN_VIS" to 0;
SET TERM !! ;
CREATE TRIGGER TRI_GEN_VIS FOR GC_VISITA
BEFORE INSERT
AS BEGIN
  NEW.COD_VISITA = GEN_ID (GEN_VIS, 1);
END !!
SET TERM ; !!

